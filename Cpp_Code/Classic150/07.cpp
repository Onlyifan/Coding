class Solution {
  public:
    int maxProfit(vector<int> &prices) {
        int small = prices[0];
        int result = 0;

        for (int i = 1; i < prices.size( ); ++i) {
            small = min(prices[i], small);
            result = max(result, prices[i] - small);
        }
        return result;
    }
};
